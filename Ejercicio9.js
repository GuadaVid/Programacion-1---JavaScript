var var1;
var1=parseInt(prompt("Ingrese el mes: "));
switch (var1)
{
    case 1:
        document.write(var1+"es el mes Enero");
    break;
    case 2:
        document.write(var1+"es el mes Febrero");
    break;
    case 3:
        document.write(var1+"es el mes Marzo");
    break;
    case 4:
        document.write(var1+"es el mes Abril");
    break;
    case 5:
        document.write(var1+"es el mes Mayo");
    break;
    case 6:
        document.write(var1+"es el mes de Junio");
    break;
    case 7:
        document.write(var1+"es el mes de Julio");
    break;
    case 8:
        document.write(var1+"es el mes de Agosto");
    break;
    case 9:
        document.write(var1+"es el mes de Septiembre");
    break;
    case 10:
        document.write(var1+"es el mes de Octubre");
    break;
    case 11:
        document.write(var1+"es el mes de Noviembre");
    break;
    case 12:
        document.write(var1+"es el mes de Diciembre");
    break;
    default:
        document.write("Mes incorrecto");
}