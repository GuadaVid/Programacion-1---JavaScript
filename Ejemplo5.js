var valor1,valor2;
valor1=parseInt(prompt("Ingrese un numero entero: "));
valor2=parseFloat(prompt("Ingrese otro numero real: "));
document.write("La suma es: "+(valor1+valor2)+"<br>");
document.write("La resta es: "+(valor1-valor2)+"<br>");
document.write("El producto es: "+valor1*valor2+"<br>");
document.write("El cociente es: "+valor1/valor2+"<br>");
document.write("El resto es: "+valor1%valor2+"<br>");